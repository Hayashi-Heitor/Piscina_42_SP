int	ft_str_is_alpha(char *str);

int	ft_str_is_alpha(char *str)
{
	char	*s;

	s = str;
	while (*s && ((*s >= 'a' && *s <= 'z') || (*s >= 'A' && *s <= 'Z')))
		s++;
	if (*s == '\0')
		return (1);
	else
		return (0);
}
